package ville;

public class RouteSensUnique extends Route{
	private String sens;
	private int taille;
	
	public RouteSensUnique() {
		
	}

	public String getSens() {
		return sens;
	}

	public void setSens(String sens) {
		this.sens = sens;
	}

	public int getTaille() {
		return taille;
	}

	public void setTaille(int taille) {
		this.taille = taille;
	}
	
	
}
