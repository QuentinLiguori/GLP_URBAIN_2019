package ville;

public class Route {
	
	private int departX;
	private int departY;
	private int arriveX;
	private int arriveY;
	
	public Route() {
		
	}

	public int getDepartX() {
		return departX;
	}

	public void setDepartX(int departX) {
		this.departX = departX;
	}

	public int getDepartY() {
		return departY;
	}

	public void setDepartY(int departY) {
		this.departY = departY;
	}

	public int getArriveX() {
		return arriveX;
	}

	public void setArriveX(int arriveX) {
		this.arriveX = arriveX;
	}

	public int getArriveY() {
		return arriveY;
	}

	public void setArriveY(int arriveY) {
		this.arriveY = arriveY;
	}
	
	
}
